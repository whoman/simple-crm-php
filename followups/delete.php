<?php
require_once '../includes/header.php';

// ?????? ID ??????
$id = isset($_GET['id']) ? intval($_GET['id']) : 0;

if ($id > 0) {
    try {
        // ????? ???? ?????? ? ?????? ???????
        $stmt = $db->prepare("SELECT f.*, c.name as client_name 
                              FROM followups f
                              JOIN clients c ON f.client_id = c.id
                              WHERE f.id = :id");
        $stmt->execute(['id' => $id]);
        $followup = $stmt->fetch();
        
        if ($followup) {
            // ??? ??????
            $stmt = $db->prepare("DELETE FROM followups WHERE id = :id");
            $stmt->execute(['id' => $id]);
            
            // ???? ??????
            $_SESSION['message'] = '?????? ????? ?? "' . $followup['client_name'] . '" ?? ?????? ??? ??.';
            $_SESSION['message_type'] = 'success';
        }
    } catch (PDOException $e) {
        $_SESSION['message'] = '??? ?? ??? ??????: ' . $e->getMessage();
        $_SESSION['message_type'] = 'danger';
    }
}

// ?????? ?? ????
header('Location: index.php');
exit;
?>